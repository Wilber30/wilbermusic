	<h2 class="mt-4 lg:my-4">Who is he?</h2>
	<p>
		Wilber Sears is a multi-instrumentalist and solo guitarist based in Oxfordshire. 
		Wilber’s music is not tied exclusively to any one genre, instead his multi-instrumental 
		compositions can vary quite drastically in instrumentation and style. For example his recent piece, 
		<i>Badinage</i>, utilises the unlikely combination of concertina, glockenspiel, recorder, guitars and assorted percussion; 
		the piece is written in a style that is somewhat reminiscent of medieval music and has a simple form. 
		<br><br>
	</p>	

	<p class="hidden lg:block">
		Alongside his multi-instrumental compositions, he also writes and arranges solo finger-style guitar instrumentals. 
		Wilber’s guitar technique initially derived from listening to country-blues guitarists of early twentieth century; 
		in particular the wonderful finger-picking of Mississippi John Hurt. However, Wilber has since arranged pieces in a 
		wide variety of styles, using different types of acoustic guitar. 
		<br><br>
	
		<i>The above was written in the third person by Wilber Sears, April 2023</i>
