	<h2 class="mt-4 lg:my-4">Who am I?</h2>
	<p>
    I am a multi-instrumentalist and solo guitarist based in Oxfordshire. My music is not tied exclusively 
    to any one genre, instead my multi-instrumental compositions can vary quite drastically in instrumentation 
    and style. For example my recent piece, <i>Badinage</i>, utilises the unlikely combination of concertina, glockenspiel, 
    recorder, guitars and assorted percussion; the piece is written in a style that is somewhat reminiscent of medieval 
    music and has a simple form. 

		<br><br>
	</p>	

	<p class="hidden lg:block">
    Alongside my multi-instrumental compositions, I also write and arrange solo finger-style guitar instrumentals. 
    My guitar technique was initially inspired by listening to country-blues guitarists of early twentieth century; 
    in particular the wonderful finger-picking of Mississippi John Hurt. However, I have since arranged pieces in a 
    wide variety of styles using many different models of acoustic guitar. 
		<br><br>
	
		<i>Wilber Sears, April 2023</i>
	</p>
